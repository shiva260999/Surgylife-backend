const mongoose = require("mongoose");
const treatmentSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  // URL or uploaded image
});
module.exports = mongoose.model("treatments", treatmentSchema);
